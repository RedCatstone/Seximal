    <h3>Random Ideas</h3>
    <p>
        <span class="bullet">></span> Different fonts, using decimals 0123456789 numbers for every base is quite confusing...

        <br><br><span class="bullet">></span> Number groupings: decimal numbers are grouped in 3s (12 000).
        Seximal numbers should be grouped in 4s (13 1320) because 6<sup>4</sup> = 1296 is actually quite close to decimal 1000.
        
        <br><br><span class="bullet">></span> Compression: most seximal numbers are a tiny bit longer than decimal numbers. This isn't really an issue,
        but for things like phone numbers it is. Here you could use a full niftimal 36-character alphabet (10 numbers + 26 letters).
        That way every 2-digit pair can be compressed into 1 digit, which works perfectly with the groupings in 4.
        This isn't pheasable in decimal because of 3 groupings and needing to learn 100-symbols.
        (125 3220 could become 1ovj)

